// Get localStorage key for storing recent emojis
const RECENT_EMOJIS_KEY = 'emoji-picker-recent';

// Load recent emojis from localStorage
const getRecentEmojis = (): string[] => {
  try {
    const stored = localStorage.getItem(RECENT_EMOJIS_KEY);
    return stored ? JSON.parse(stored) : [];
  } catch {
    return [];
  }
};

// Save recent emojis to localStorage
const saveRecentEmojis = (emojis: string[]) => {
  try {
    localStorage.setItem(RECENT_EMOJIS_KEY, JSON.stringify(emojis));
  } catch {
    // localStorage not available or quota exceeded
  }
};

// Add emoji to recent list
const addToRecent = (emoji: string) => {
  const recent = getRecentEmojis();
  const filtered = recent.filter(e => e !== emoji);
  const updated = [emoji, ...filtered].slice(0, 20); // Keep last 20
  saveRecentEmojis(updated);
};

export interface EmojiPickerProps {
  selectedEmoji?: string;
  onEmojiSelect: (emoji: string) => void;
  className?: string;
}

export const EmojiPicker: React.FC<EmojiPickerProps> = ({
  selectedEmoji,
  onEmojiSelect,
  className
}) => {
  const [searchTerm, setSearchTerm] = useState('');
  const [activeCategory, setActiveCategory] = useState('smileys');
  const [recentEmojis, setRecentEmojis] = useState<string[]>(getRecentEmojis());

  // Update recent emojis category
  EMOJI_CATEGORIES.recent.emojis = recentEmojis;

  const handleEmojiSelect = (emoji: string) => {
    onEmojiSelect(emoji);
    addToRecent(emoji);
    setRecentEmojis(getRecentEmojis());
  };

  const filteredEmojis = useMemo(() => {
    if (!searchTerm.trim()) {
      return EMOJI_CATEGORIES[activeCategory as keyof typeof EMOJI_CATEGORIES]?.emojis || [];
    }
    
    const query = searchTerm.toLowerCase().trim();
    const allEmojis = Object.values(EMOJI_CATEGORIES).flatMap(cat => cat.emojis);
    
    // Simple search by emoji name would require a mapping - for now just return current category
    // In a production app, you'd want to include emoji name/keyword mappings
    return EMOJI_CATEGORIES[activeCategory as keyof typeof EMOJI_CATEGORIES]?.emojis || [];
  }, [searchTerm, activeCategory]);

  const clearSearch = () => {
    setSearchTerm('');
  };

  return (
    <div className={cn("w-full max-w-sm", className)}>
      {/* Search Input */}
      <div className="relative mb-3">
        <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 w-4 h-4 text-muted-foreground" />
        <Input
          type="text"
          placeholder="Search emojis..."
          value={searchTerm}
          onChange={(e) => setSearchTerm(e.target.value)}
          className="pl-10 pr-10"
        />
        {searchTerm && (
          <Button
            variant="ghost"
            size="sm"
            onClick={clearSearch}
            className="absolute right-1 top-1/2 transform -translate-y-1/2 h-7 w-7 p-0"
          >
            <X className="w-3 h-3" />
          </Button>
        )}
      </div>

      {/* Category Tabs */}
      <div className="flex flex-wrap gap-1 mb-3 border-b">
        {Object.entries(EMOJI_CATEGORIES).map(([key, category]) => {
          // Skip recent if empty
          if (key === 'recent' && category.emojis.length === 0) return null;
          
          return (
            <Button
              key={key}
              variant={activeCategory === key ? "default" : "ghost"}
              size="sm"
              onClick={() => setActiveCategory(key)}
              className={cn(
                "h-8 px-2 text-xs",
                activeCategory === key && "bg-primary text-primary-foreground"
              )}
            >
              {category.name}
            </Button>
          );
        })}
      </div>

      {/* Emojis Grid */}
      <div className="max-h-64 overflow-y-auto border rounded-md">
        {filteredEmojis.length === 0 ? (
          <div className="p-6 text-center text-muted-foreground text-sm">
            {activeCategory === 'recent' && recentEmojis.length === 0 
              ? 'No recent emojis. Select some emojis to see them here.' 
              : searchTerm 
                ? 'No emojis found. Try a different search term.' 
                : 'No emojis in this category.'}
          </div>
        ) : (
          <div className="grid grid-cols-8 gap-1 p-2">
            {filteredEmojis.map((emoji, index) => (
              <Button
                key={`${emoji}-${index}`}
                variant="ghost"
                size="sm"
                onClick={() => handleEmojiSelect(emoji)}
                className={cn(
                  "h-10 w-10 p-0 hover:bg-accent text-lg font-normal",
                  selectedEmoji === emoji && "bg-accent border-2 border-primary"
                )}
                title={emoji}
              >
                {emoji}
              </Button>
            ))}
          </div>
        )}
      </div>
    </div>
  );
};

